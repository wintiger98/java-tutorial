package variable;

public class Var7 {

  public static void main(String[] args) {
    int a = 100;
    double b = 10.5;
    boolean c = true;
    char d = 'A';
    String e = "Hello java";

    System.out.println("a = " + a);
    System.out.println("b = " + b);
    System.out.println("c = " + c);
    System.out.println("d = " + d);
    System.out.println("e = " + e);
  }
}
